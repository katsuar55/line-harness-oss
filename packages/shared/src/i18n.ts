/**
 * Get translation for a key in a given language
 */
export function t(lang: Lang, key: TranslationKey): string {
  return translations[lang]?.[key] ?? translations.ja[key] ?? key;
}

/**
 * Get all translations for a language
 */
export function getTranslations(lang: Lang): Record<TranslationKey, string> {
  return translations[lang] ?? translations.ja;
}

/**
 * Detect language from LINE profile or browser
 */
export function detectLanguage(lineLanguage?: string | null): Lang {
  if (!lineLanguage) return 'ja';
  const lang = lineLanguage.toLowerCase().slice(0, 2);
  if (lang === 'ja') return 'ja';
  if (lang === 'en') return 'en';
  if (lang === 'ko') return 'ko';
  if (lang === 'zh') return 'zh';
  if (lang === 'th') return 'th';
  return 'ja';
}
